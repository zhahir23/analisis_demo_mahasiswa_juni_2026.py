for idx, row in cluster_docs.iterrows():

    skor = tfidf_matrix[idx].toarray().flatten()
    urutan = skor.argsort()[::-1]

    keywords = []

    for i in urutan:
        kata = fitur_kata[i]

        if kata.startswith("@"):
            continue

        if kata not in keywords:
            keywords.append(kata)

        if len(keywords) == 5:
            break

